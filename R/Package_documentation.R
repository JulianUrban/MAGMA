#' Framwork includes four balance criteria, namely *Pillai's Trace*, *d-ratio*,
#' *mean g*, and *adjusted d-ratio*. It is possible display these balance
#' criteria in an APA table. Lastly, one function creates plots to show the
#' balance trend for different matching scenarios.
